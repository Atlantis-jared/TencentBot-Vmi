#pragma once
// =============================================================================
// VisionEngine.h — 视觉识别引擎（截图 + OpenCV 模板匹配）
// =============================================================================
// 本模块封装所有与"看游戏画面"相关的逻辑：
//   1. DxgiWindowCapture 截图
//   2. 地图模板加载与匹配（识别当前所在地图）
//   3. 地图 UI 定位（供 TencentBot 换算小地图坐标点击）
//   4. NPC 模板配置、加载、全屏匹配、ROI 区域匹配
//
// 使用方式：
//   VisionEngine vision(capture);          // 注入 DxgiWindowCapture 引用
//   vision.loadAllTemplates();             // 初始化时调用一次
//   std::string map = vision.getCurrentMapName(); // 识别当前地图
//   auto pts = vision.findNpcOnScreen("驿站老板"); // 找 NPC
//
// 依赖：
//   - DxgiWindowCapture.h（屏幕截图接口）
//   - OpenCV（图像处理 / 模板匹配）
//   - BotLogger.h（统一日志）
// =============================================================================

#include <string>
#include <vector>
#include <map>
#include <opencv2/core/mat.hpp>
#include "DxgiWindowCapture.h"
#include "CaptchaEngine.h"    // 复用 Point2D 结构体

// -----------------------------------------------------------------------------
// NpcTemplateConfig — NPC 模板的静态配置（磁盘路径 + 匹配阈值）
// 只需在 initNpcConfigs() 中配置一次，后续自动加载。
// -----------------------------------------------------------------------------
struct NpcTemplateConfig {
    std::string imagePath; // NPC 模板图片相对路径，例如 "npcs/yizhanlaoban.png"
    double matchThreshold; // 模板匹配的最低分数阈值（0~1），推荐 0.7~0.85
};

// -----------------------------------------------------------------------------
// MapUiLocation — locateMapUiOnScreen() 的返回值
// 描述小地图 UI 控件在屏幕上的位置（用于换算点击坐标）
// -----------------------------------------------------------------------------
struct MapUiLocation {
    cv::Point topLeftCorner; // UI 控件左上角的屏幕像素坐标
    int       uiHeight;      // UI 控件的像素高度（用于换算 Y 轴）
    bool      found;         // 匹配是否成功（false 时其他字段无效）
};

// =============================================================================
// VisionEngine
// =============================================================================
class VisionEngine {
public:
    // -------------------------------------------------------------------------
    // 构造函数：注入截图对象的引用，避免复制昂贵的 D3D 资源。
    // TencentBot 持有 DxgiWindowCapture 的所有权，VisionEngine 只借用引用。
    // -------------------------------------------------------------------------
    explicit VisionEngine(DxgiWindowCapture& screenCapture);

    // -------------------------------------------------------------------------
    // loadAllTemplates() — 初始化时调用一次，加载所有模板到内存缓存中
    // 包含：地图模板（maps/ 目录下的 .png）和 NPC 模板（npcs/ 目录中配置的文件）
    // -------------------------------------------------------------------------
    void loadAllTemplates();

    // =========================================================================
    // 地图识别接口
    // =========================================================================

    // -------------------------------------------------------------------------
    // getCurrentMapName() — 截取当前屏幕，与所有地图模板做模板匹配，返回地图名
    // 返回值：
    //   - 地图名字符串（与 MAP_PROPERTIES / kMapYBase 的 key 对应）
    //   - "Unknown"    — 匹配分数低于阈值 0.8，无法识别
    //   - "CaptureEmpty" — 截图获取失败（窗口可能最小化或丢失）
    // -------------------------------------------------------------------------
    std::string getCurrentMapName();

    // -------------------------------------------------------------------------
    // locateMapUiOnScreen() — 在屏幕上找到小地图 UI 控件的精确位置
    // 参数：
    //   mapName — 目标地图名（用于选取对应的 UI 模板文件）
    // 返回：
    //   MapUiLocation（含左上角坐标和高度），found=false 时表示未找到
    // 说明：
    //   采用日/夜双模板策略（mapName.png 和 mapName1.png），取分数更高的。
    // -------------------------------------------------------------------------
    MapUiLocation locateMapUiOnScreen(const std::string& mapName);

    // =========================================================================
    // NPC 识别接口
    // =========================================================================

    // -------------------------------------------------------------------------
    // findNpcOnScreen() — 全屏查找指定 NPC，返回最佳匹配位置列表
    // 参数：
    //   npcName — NPC 名称（需在 initNpcConfigs() 中有对应配置）
    // 返回：
    //   Point2D 列表；当前实现最多返回 1 个结果（best match），
    //   若分数低于阈值则返回空列表。
    // -------------------------------------------------------------------------
    std::vector<Point2D> findNpcOnScreen(const std::string& npcName);

    // -------------------------------------------------------------------------
    // findNpcInScreenRegion() — 在指定屏幕矩形区域内查找 NPC
    // 参数：
    //   npcName     — NPC 名称（需在 initNpcConfigs() 中有对应配置）
    //   regionX/Y   — 搜索矩形左上角的屏幕坐标（像素）
    //   regionW/H   — 搜索矩形的宽度和高度（像素）
    // 返回：
    //   Point2D；若未找到则 score <= 0，x/y = -1。
    // 注意：
    //   返回的 x/y 是相对于全屏的坐标（已加回 regionX/Y 偏移）。
    // -------------------------------------------------------------------------
    Point2D findNpcInScreenRegion(const std::string& npcName,
                                  int regionX, int regionY,
                                  int regionW, int regionH);

    // =========================================================================
    // 截图辅助
    // =========================================================================

    // -------------------------------------------------------------------------
    // captureToBuffer() — 将当前屏幕数据写入内部缓存（imageBuffer/frameW/frameH）
    // 返回：截图是否成功
    // -------------------------------------------------------------------------
    bool captureToBuffer();

    // 当前帧画面数据（BGRA 格式，大小 = frameW * frameH * 4 字节）
    std::vector<uint8_t> imageBuffer;
    int frameW = 0; // 当前帧宽度（像素）
    int frameH = 0; // 当前帧高度（像素）

private:
    // -------------------------------------------------------------------------
    // 内部初始化函数（由 loadAllTemplates() 调用）
    // -------------------------------------------------------------------------
    void loadMapTemplatesFromDisk();  // 从 maps/ 目录加载地图识别模板
    void initNpcConfigs();            // 配置所有 NPC 的模板路径和阈值
    void loadNpcTemplatesFromDisk();  // 按配置加载 NPC 模板图片

    // -------------------------------------------------------------------------
    // 截图对象引用（TencentBot 持有所有权，VisionEngine 只借用）
    // -------------------------------------------------------------------------
    DxgiWindowCapture& screenRef;

    // -------------------------------------------------------------------------
    // 模板缓存（一次加载，运行期反复使用，避免重复 I/O）
    // -------------------------------------------------------------------------
    std::map<std::string, cv::Mat> mapTemplates;        // 地图识别模板：{地图名 → 图像矩阵}
    std::map<std::string, cv::Mat> mapUiTemplateCache;  // 地图 UI 模板缓存（locateMapUiOnScreen 用）
    std::map<std::string, cv::Mat> npcTemplates;        // NPC 识别模板：{NPC名 → 图像矩阵}
    std::map<std::string, NpcTemplateConfig> npcConfigs; // NPC 静态配置表
};
